// Guardar el contenido español original (del HTML) para poder volver
const ES = {};
document.querySelectorAll('[data-i18n]').forEach((el) => {
  ES[el.getAttribute('data-i18n')] = el.innerHTML;
});

function setLanguage(lang) {
  const dict = lang === 'en' ? EN : ES;
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] != null) el.innerHTML = dict[key];
  });
  document.documentElement.lang = lang;
  document.querySelectorAll('.lang-toggle button').forEach((b) => {
    const active = b.getAttribute('data-lang') === lang;
    b.classList.toggle('is-active', active);
    b.setAttribute('aria-pressed', String(active));
  });
  try { localStorage.setItem('lang', lang); } catch (e) {}
}

document.querySelectorAll('.lang-toggle button').forEach((b) => {
  b.addEventListener('click', () => setLanguage(b.getAttribute('data-lang')));
});

// Idioma inicial: el guardado por el usuario, o según el idioma del navegador
const saved = (() => { try { return localStorage.getItem('lang'); } catch (e) { return null; } })();
setLanguage(saved || ((navigator.language || '').toLowerCase().startsWith('en') ? 'en' : 'es'));

// ===== Año dinámico en el footer =====
document.getElementById('year').textContent = new Date().getFullYear();

// ===== Menú móvil =====
const toggle = document.querySelector('.nav__toggle');
const links = document.querySelector('.nav__links');
if (toggle && links) {
  toggle.addEventListener('click', () => {
    const open = links.classList.toggle('is-open');
    toggle.setAttribute('aria-expanded', String(open));
  });
  links.querySelectorAll('a').forEach((a) =>
    a.addEventListener('click', () => {
      links.classList.remove('is-open');
      toggle.setAttribute('aria-expanded', 'false');
    })
  );
}

// ===== Animaciones de aparición al hacer scroll =====
const revealTargets = document.querySelectorAll(
  '.section__head, .prose, .card, .feature, .t-item, .contact-card, .hero__stats'
);
revealTargets.forEach((el) => el.setAttribute('data-reveal', ''));

const io = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add('is-visible');
        io.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.12 }
);
revealTargets.forEach((el) => io.observe(el));

// ===== Sombra del nav al hacer scroll =====
const nav = document.querySelector('.nav');
const onScroll = () => {
  if (window.scrollY > 10) nav.style.boxShadow = '0 8px 30px -18px rgba(15,30,61,.4)';
  else nav.style.boxShadow = 'none';
};
window.addEventListener('scroll', onScroll, { passive: true });
onScroll();
